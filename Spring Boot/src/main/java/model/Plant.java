package model;

public class Plant {
    private String name;
    private String plantID;
    
    public String getName() {
        return name;
    }
    public String getPlantID() {
        return plantID;
    }
    public void setPlantID(String plantID) {
        this.plantID = plantID;
    }
    public void setName(String name) {
        this.name = name;
    }
    
}
